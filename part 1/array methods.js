let fruits = ["apple", "grapes", "banana"];
console.log(fruits);

// adds value to the last of array
fruits.push("litchi");
console.log(fruits);
fruits.push("berry", "melon");
console.log(fruits);

// removes last value of array
fruits.pop();
console.log(fruits);
let removedFruit = fruits.pop();
console.log(removedFruit);

// adds value to the start of array
fruits.unshift("pear");
console.log(fruits);
fruits.unshift("jamun", "kiwi");
console.log(fruits);

// removes first index value
fruits.shift();
console.log(fruits);